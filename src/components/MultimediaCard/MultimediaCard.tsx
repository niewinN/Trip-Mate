const capturePhoto = async () => {
  if (webcamRef.current) {
    const imageSrc = webcamRef.current.getScreenshot();
    if (imageSrc) {
      setMediaPreview(imageSrc);
      setMediaType('image');

      try {
        // Przekszta≈Çƒá dane base64 na Blob
        const response = await fetch(imageSrc);
        const blob = await response.blob();
        const file = new File([blob], `photo-${Date.now()}.png`, { type: 'image/png' });

        console.log('‚úÖ Photo captured successfully');

        // Przekazanie pliku bezpo≈õrednio do funkcji onUpload
        if (onUpload) {
          await onUpload(file);
        }
      } catch (error) {
        console.error('‚ùå Error capturing photo:', error);
      }

      stopCamera();
    }
  }
};


  /** üé• Rozpoczƒôcie nagrywania */
  const startRecording = () => {
    if (webcamRef.current && webcamRef.current.stream) {
      recordedChunks.current = [];
      const mediaRecorder = new MediaRecorder(webcamRef.current.stream, {
        mimeType: 'video/webm',
      });
      mediaRecorderRef.current = mediaRecorder;
      mediaRecorder.start();

      mediaRecorder.ondataavailable = (event) => {
        if (event.data.size > 0) {
          recordedChunks.current.push(event.data);
        }
      };

      setIsRecording(true);
    }
  };

  /** üõë Zatrzymanie nagrywania */
