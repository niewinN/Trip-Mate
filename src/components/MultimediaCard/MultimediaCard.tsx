const handleFileUpload = async (event: React.ChangeEvent<HTMLInputElement>) => {
  const file = event.target.files?.[0];
  if (file && onUpload) {
    try {
      // Przeka≈º plik do nadrzƒôdnej funkcji `onUpload`
      await onUpload(file);
    } catch (error) {
      console.error('‚ùå Error handling file upload in parent:', error);
    }
  }
  resetCameraState();
};

  /** üé• Aktywacja kamery */
  const startCamera = () => {
    resetCameraState();
    setIsCameraActive(true);
  };

  /** üì∏ Zrobienie zdjƒôcia */
